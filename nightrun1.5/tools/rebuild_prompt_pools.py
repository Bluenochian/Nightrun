def clean_line(raw):
    line = " ".join(str(raw).replace("\t", " ").split()).strip(" ,")
    line = line.replace(f"{MATURE_WORD.capitalize()}-only", "Glamour")
    line = line.replace(f"{MATURE_WORD}-only", "glamour")
    line = line.replace(MATURE_WORD, "").replace(MATURE_WORD.capitalize(), "")
    line = line.replace("  ", " ")
    line = line.replace(", ,", ",")
    line = line.strip(" ,")
    lowered = line.lower()
    if any(term in lowered for term in BANNED_POOL_TERMS):
        return ""
    return line


def clean_header(raw):
    line = " ".join(str(raw).replace("\t", " ").split()).strip(" ,")
    line = line.replace(f"{MATURE_WORD.capitalize()}-only", "Glamour")
    line = line.replace(f"{MATURE_WORD}-only", "glamour")
    line = line.replace(MATURE_WORD, "").replace(MATURE_WORD.capitalize(), "")
    return line.strip(" ,")


def unique(lines):
    seen = set()
    out = []
    for raw in lines:
        line = clean_line(raw)
        if not line or line.startswith("#"):
            continue
        key = line.lower()
        if key not in seen:
            out.append(line)
            seen.add(key)
    return out


def unique_limit(lines, limit):
    seen = set()
    out = []
    for raw in lines:
        line = clean_line(raw)
        if not line or line.startswith("#"):
            continue
        key = line.lower()
        if key in seen:
            continue
        out.append(line)
        seen.add(key)
        if len(out) >= limit:
            break
    return out


def write_pool(name, header, lines):
    path = POOLS / name
    clean = unique(lines)
    text = "# " + clean_header(header) + "\n" + "\n".join(clean) + "\n"
    path.write_text(text, encoding="utf-8")
    return name, len(clean)


def first_n(lines, n):
    return unique_limit(lines, n)


